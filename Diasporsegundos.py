#4. REALIZAR PROGRAMAQUE CONVIERTE DIASA SEGUNDOS.

print("4. REALIZAR PROGRAMAQUE CONVIERTE DIASA SEGUNDOS.")

dia=int(input("Escriba los dias: "))

segundos=dia*(24*60*60)
minutos=dia*(24*60)
horas=dia*(24)


print(str(segundos),"Segundos por",dia,"dias")
print(str(minutos),"Minutos por",dia,"dias")
print(str(horas),"Horas por",dia,"dias")

